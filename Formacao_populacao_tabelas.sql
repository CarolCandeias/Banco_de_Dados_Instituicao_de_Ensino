CREATE TABLE "alunos" (
  "id_aluno" int PRIMARY KEY,
  "id_turma" int,
  "nome" varchar(100),
  "sobrenome" varchar(100),
  "cpf" varchar(11),
  "data_nascimento" date,
  "genero" varchar(2),
  "endereco" varchar(500),
  "cidade" varchar(50),
  "estado" varchar(2),
  "numero" varchar(16),
  "login_portal" varchar(100),
  "email" varchar(250),
  "id_meio_de_pagamento" int
 
);


CREATE TABLE "ementa" (
  "id_ementa" int PRIMARY KEY,
  "id_curso" int,
  "modulo" varchar(8),
  "descricao" varchar(200)
  
);


CREATE TABLE "pagamento_aluno" (
  "id_meio_de_pagamento" int PRIMARY KEY,
  "meio_de_pagamento" varchar(30),
  "valor" varchar(20)
);


CREATE TABLE "formacao_aluno" (
  "id_aluno" int,
  "situacao_curso" varchar(20),
  "situacao_financeira" varchar(20)
  
);


CREATE TABLE "departamentos" (
  "id_departamento" int PRIMARY KEY,
  "nome_departamento" varchar(100),
  "sigla" varchar(3)
);


CREATE TABLE "funcionarios" (
  "id_funcionario" int PRIMARY KEY,
  "id_departamento" int,
  "nome_funcionario" varchar(100),
  "sobrenome_funcionario" varchar(100),
  "cpf" varchar(11),
  "data_nascimento" date,
  "genero" varchar(2),
  "numero" varchar(16),
  "login_portal" varchar(100),
  "numero_conta" varchar(8),
  "agencia" varchar(6),
  "banco" varchar(50),
  "salario" varchar(20)

);


CREATE TABLE "cursos" (
  "id_curso" int PRIMARY KEY,
  "curso" varchar(50)
);

CREATE TABLE "modulo" (
    id_modulo int primary key,
    id_turma int,
    id_ementa int,
    data_inicio date,
    data_conclusao date,
    status varchar(20),
    id_funcionario int
);

CREATE TABLE "turma" (
  "id_turma" int PRIMARY KEY,
  "id_curso" int,
  "nome_turma" varchar(10),
  "status" varchar(20),
  "periodo" varchar(20),
  "id_funcionario" int

);

alter table alunos add foreign key (id_turma) references turma (id_turma);
alter table alunos add foreign key (id_meio_de_pagamento) references pagamento_aluno (id_meio_de_pagamento);
alter table ementa add foreign key (id_curso) references cursos (id_curso);
alter table formacao_aluno add foreign key (id_aluno) references alunos (id_aluno);
alter table funcionarios add foreign key (id_departamento) references departamentos (id_departamento);
alter table modulo add foreign key (id_turma) references turma (id_turma);
alter table modulo add foreign key (id_ementa) references ementa (id_ementa);
alter table modulo add foreign key (id_funcionario) references funcionarios (id_funcionario);
alter table turma add foreign key (id_curso) references cursos (id_curso);
alter table turma add foreign key (id_funcionario) references funcionarios (id_funcionario);



